/**
 * 銀行コードから銀行名を検索
 * @param code 4桁の銀行コード
 * @returns 銀行名、見つからない場合はnull
 */
export function getBankNameByCode(code: string): string | null {
  if (!code || code.length !== 4) {
    return null;
  }
  return BANK_CODES[code] || null;
}

/**
 * 銀行名から部分一致で検索
 * @param name 検索する銀行名（部分一致）
 * @returns マッチした銀行情報の配列
 */
export function searchBanksByName(name: string): BankInfo[] {
  if (!name) {
    return [];
  }
  const results: BankInfo[] = [];
  for (const [code, bankName] of Object.entries(BANK_CODES)) {
    if (bankName.includes(name)) {
      results.push({ code, name: bankName });
    }
  }
  return results;
}
